          <h2>Faculty of Precision Engineering</h2>
          <p class="text-justify">Precision engineering and manufacturing issues are becoming ever more important in current and future technologies. Precision engineering is defined as painstaking attention to details and requires knowledge of a wide variety of measurement, fabrication, and control issues.  Precision manufacturing is defined as the manufacture of individual pieces with extreme accuracy. This type of machining is used to make parts for various machines, including medical, aeronautical, and any other industry that requires identical parts to be created in large quantities.<br><br>Precision engineering is a sub-discipline of mechanical engineering, electrical engineering, software engineering, electrical engineering, control engineering, and optical engineering. It is concerned with designing machines, fixture, and other structures that have exceptionally high tolerances, are repeatable, and are stable over time. This engineering field will aid in the advancement of various technologies that need to gain industrial competitiveness.</p>
          <h4>Precision engineering focuses on many areas such as:</h4>
          <ul>
              <li>Research</li>
              <li>Product Design & Development</li>
              <li>System Design</li>
              <li>Manufacturing and Measurement of  high accuracy Components and Systems</li>
          </ul>
          <h4>Precision engineering provides the following goals:</h4>
          <ul>
              <li>Create a highly precise movement.</li>
              <li>Reduce the dispersion of the product's or part's function.</li>
              <li>Eliminate fitting and promote assembly, especially automatic assembly.</li>
              <li>Reduce the initial cost & running cost.</li>
              <li>Extend the life span.</li>
              <li>Enable the design safety factor to be lowered.</li>
              <li>Improve interchangeability of components so that corresponding parts made by other factories or firms can be used in their place.</li>
              <li>Improve quality control through higher machine accuracy capabilities and hence reduce scrap, rework, and conventional inspection.</li>
              <li>Achieve a greater wear/fatigue life of components.</li>
              <li>Achieve greater miniaturization and packing densities.</li>
              <li>Achieve further advances in technology and the underlying sciences.</li>
          </ul>
