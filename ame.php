          <p class="text-justify">Materials that are utilized in high-technology applications are termed advanced materials. By high technology we mean a device or product that operates or functions using relatively intricate and sophisticated principles; examples include electronic equipment, computers, fiber-optic systems, spacecraft, aircraft, and military rocketry. These advanced materials are typically traditional materials whose properties have been enhanced, and, also newly developed, high-performance materials. Furthermore, they may be of all material types, and are normally expensive. Advanced materials include semiconductors, biomaterials, and “materials of the future” (that is, smart materials and nanoengineered materials). The properties and applications of a number of these advanced materials - for example, materials that are used for lasers, integrated circuits, magnetic information storage, liquid crystal displays (LCDs), and fiber optics need to study.<br><br>Processing, Structure, Properties and Performance are the main discipline of materials science and engineering and their interrelationship. There is a recognized need to find new, economical sources of energy and to use present resources more efficiently. Materials will undoubtedly play a significant role in these developments.<br><br>For example, the direct conversion of solar power into electrical energy has been demonstrated. Solar cells employ some rather complex and expensive materials. To ensure a viable technology, materials that are highly efficient and less costly in this conversion process must be developed. The hydrogen fuel cell is other very attractive and feasible energy conversion technology that has the advantage of being nonpolluting.<br><br>Environmental quality depends on our ability to control air and water pollution. Pollution control techniques employ various materials. In addition, materials processing and refinement methods need to be improved so that they produce less environmental degradation. Therefore, the roles of material engineers and metallurgists play relative to these, as well as other environmental and societal issues.</p>
